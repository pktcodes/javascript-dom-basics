// LET VS CONST VS VAR

// using var
var value = "some value";
value = "some other value";

// using let
let name = "john";
name = "peter";

// using const (CONSTANT) - can't re-assign
const lastName = "smith";
// lastName = "peter"; ❌
// const firstName; ❌

console.log(value);
console.log(name);
console.log(lastName);
